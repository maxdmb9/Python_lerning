#Сортировка списка
#Изменения не обратимы
cars = ['bmw','audi','toyota','subaru']
cars.sort()
print(cars)
#Сортировка в обратном порядке
#Изменения не обратимы
cars = ['bmw','audi','toyota','subaru']
cars.sort(reverse=True)
print(cars)
#Временная сортировка
cars = ['bmw','audi','toyota','suzuki']
print("Here is the original list:")
print(cars)
print("\nHere is the sorted list:")
print(sorted(cars))
print("\nHere is the original list again:")
print(cars)
#Сортировка списка в обратном порядке
cars = ['bmw','audi','toyota','suzuki']
print(cars)
#Реверс
cars.reverse()
print(cars)
#Обратно
cars.reverse()
print(cars)
